package edu.ycp.cs320.gamingwebsite.shared;

public class Player {
	private double x;
	private double y;
	
	public Player(){
		this.x = 500;
		this.y = 350;
	}
	
	public double getX() {
		return x;
	}
	
	public double getY() {
		return y;
	}
	
	public void setX(double x) {
		this.x = x;
	}
	
	public void setY(double y) {
		this.y = y;
	}
	

}
